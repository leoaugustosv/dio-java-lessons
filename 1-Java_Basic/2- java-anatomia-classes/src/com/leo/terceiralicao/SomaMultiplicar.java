package com.leo.terceiralicao;

public class SomaMultiplicar {


    // LEONARDO AUGUSTO SILVEIRA
    public static void main(String[] args) {
        int a = 5;
        int b = 3;  

        int soma = a+b;
        int multiplicacao = a*b;
        int resultMultiplicar = soma * multiplicacao;
        int resultSomar = soma + multiplicacao;

        System.out.println("A soma de A e B é: " + soma);
        System.out.println("A multiplicação de A e B é: " + multiplicacao);
        System.out.println("A soma entre as duas operações anteriores é: " + resultSomar);
        System.out.println("A multiplicação entre as duas operações anteriores é: " + resultMultiplicar);
    }
    
}
